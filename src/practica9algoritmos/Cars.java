    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        //modify
        if (jTextField10.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Espacio en blanco", "Error", 1);
        } else {
            if (!jTextField6.getText().isEmpty()) {
                c.setProp(jTextField10.getText(), jTextField6.getText());
            } 
            if (!jTextField7.getText().isEmpty()) {
                try {
                    int year = Integer.parseInt(jTextField7.getText());
                    c.setYear(jTextField10.getText(), year);
                } catch (NumberFormatException a) {
                    JOptionPane.showMessageDialog(null, a.getMessage(), "Error", 1);
                }

            } 
            if (!jTextField8.getText().isEmpty()) {
                c.setProp(null, jTextField6.getText());

                try {
                    int costo = Integer.parseInt(jTextField8.getText());
                    c.setCosto(jTextField10.getText(), costo);
                } catch (NumberFormatException a) {
                    JOptionPane.showMessageDialog(null, a.getMessage(), "Error", 1);
                }

            } 
        }

    }//GEN-LAST:event_jButton5ActionPerformed

