    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTextField9.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Espacio en blanco", "Error", 1);
        } else {
            String search = jTextField9.getText();
            Car con = c.search(search);
            if (c == null) {
                JOptionPane.showMessageDialog(null, "No existe ese auto", "Error", 1);
            } else {
                jTable1.setValueAt(con.getPlacas(), 0, 0);
                jTable1.setValueAt(con.getPropietario(), 0, 1);
                jTable1.setValueAt(con.getYear(), 0, 2);
                jTable1.setValueAt(con.getCosto(), 0, 3);
                jTable1.setValueAt(con.getTenencia(), 0, 4);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Espacios en blanco", "Error", 1);
        } else {
            try {
                int year = Integer.parseInt(jTextField3.getText());
                int costo = Integer.parseInt(jTextField4.getText());
                c.alta(jTextField1.getText(), jTextField2.getText(), year, costo);
            } catch (NumberFormatException a) {
                JOptionPane.showMessageDialog(null, a.getMessage(), "Error", 1);

            }
        }
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField2.setText("");
        jTextField1.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

